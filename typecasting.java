public class typecasting {
    public static void main(String[]args){
        double mydouble = 9.78d;
        int myint = (int) mydouble;
        int my=9;
        double myd=my;
        System.out.println(my);
        System.out.println(myd);
        System.out.println(mydouble);
        System.out.println(myint);
    }
    
}
